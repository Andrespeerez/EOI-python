# Declaración de variables
numero = 10
Numero = 20
saludo = 'Hola mundo !!!'

# Mostramos el contenido de las variables
print(numero) # 10
print(Numero) # 20
print(Numero - 25)
print(saludo) # 'Hola mundo !!!'
print('Andrés dice : '+saludo)

#Mostramos el tipo de las variables
print(type(numero))
print(type(saludo))
print(type(None))
print()
#Mostrar typos de datos
print(type(3))
print(type(3.1))
print(type("3"))
print(type("tres"))
print(type(3==3))
print(type(('1', '2', '3')))
print(type(["1", "2", "3"]))
print(type({"1", "2", "3"}))
print(type([1, 2, 3]))
print(type({"valor1" : 2,
            "segundo": 3}))


